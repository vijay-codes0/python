# TUPLE METHODS

# create tuple
t = (10, 20, 30, 20, 40, 20)

print("Tuple:", t)

# 1 count() – counts how many times element appears
print("Count of 20:", t.count(20))


# 2 index() – returns index position of first occurrence
t2 = (10, 20, 30, 40, 50)

print("Tuple:", t2)
print("Index of 30:", t2.index(30))
