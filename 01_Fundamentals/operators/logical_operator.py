# LOGICAL OPERATORS

# and operator
# Returns True if both conditions are True
a = 10
b = 20

print("AND Operator:", a > 5 and b > 10)


# or operator
# Returns True if at least one condition is True
print("OR Operator:", a > 15 or b > 10)


# not operator
# Reverses the result
print("NOT Operator:", not(a > 5))
