# ASSIGNMENT OPERATORS

a = 10
print("Initial value:", a)

# += operator
a += 5
print("After a += 5 :", a)

# -= operator
a -= 3
print("After a -= 3 :", a)

# *= operator
a *= 2
print("After a *= 2 :", a)

# /= operator
a /= 4
print("After a /= 4 :", a)

# %= operator
a %= 3
print("After a %= 3 :", a)

# **= operator
a **= 2
print("After a **= 2 :", a)

# //= operator
a //= 2
print("After a //= 2 :", a)
