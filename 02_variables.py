'''Python Variables Examples.'''
# Assigning values to variables

name = "John"
age = 22
salary = 25000.50
is_student = True

print("Name:", name)
print("Age:", age)
print("Salary:", salary)
print("Is Student:", is_student)


#Reassigning Variable

a = 10
print("Initial value:", a)

a = 20
print("Updated value:", a)


#Multiple Assignment

a, b, c = 10, 20, 30
print(a, b, c)

#Can we change variable type

A = 10
A = "Python"
print(A)
print(type(A))










